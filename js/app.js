function imprimirReceta(boton){

let receta = boton.closest(".receta");

let contenido = receta.innerHTML;

let ventana = window.open("");

ventana.document.write(`
<html>
<head>

<title>Receta</title>

<style>

body{
font-family:Arial;
padding:30px;
}

img{
max-width:100%;
}

button{
display:none;
}

</style>

</head>

<body>

${contenido}

</body>

</html>
`);

ventana.document.close();

ventana.print();

}